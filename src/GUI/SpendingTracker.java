    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // TODO add your handling code here:
        int ri = table.getSelectedRow();

        if (ri != -1) {
            int r = JOptionPane.showConfirmDialog(null, "Do you Really Wanna Delete?", "Deletion Confirmation",
                    JOptionPane.YES_NO_OPTION);

            if (r == JOptionPane.YES_OPTION) {
                int id = (int) table.getValueAt(ri, 0);
                try {
                    DB.DbConnection.s.executeUpdate("delete from spendings where sid=" + id);
                    JOptionPane.showMessageDialog(null, "Delete Successfully ");

                    DB.DbConnection.s.execute("SET @new_sid = 0;");
                    DB.DbConnection.s.executeUpdate("UPDATE spendings SET sid = (@new_sid := @new_sid + 1) ORDER BY sid;");
                    DB.DbConnection.s.execute("ALTER TABLE spendings AUTO_INCREMENT = 1;");
                    getEntries();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please first select row ");
        }
    }//GEN-LAST:event_removeActionPerformed


