    private void displayCategory() {
        try {
            category.removeAllItems();
            ResultSet rs = DB.DbConnection.s.executeQuery("select * from category_info");
            while (rs.next()) {
                category.addItem(rs.getString("category"));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void getEntries() {
        try {
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) table.getModel();
            int rc = dtm.getRowCount();
            while (rc-- != 0) {
                dtm.removeRow(0);
            }

            java.time.LocalDate cd = java.time.LocalDate.now();//current date
            java.time.LocalDate bd = cd.minusDays(30);//beforedate
            ResultSet rs = DB.DbConnection.s.executeQuery("select * from spendings where s_date<='" + cd + "'"
                    + " and s_date>='" + bd + "' order by s_date asc");

            int total = 0;

            while (rs.next()) {
                int t = rs.getInt("amount");
                String category = rs.getString("s_category").toUpperCase();
                total += t;
                Object o[] = {rs.getInt("sid"), rs.getDate("s_date"), category, t};
                dtm.addRow(o);
            }
            totalAmount.setText(total + "");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

