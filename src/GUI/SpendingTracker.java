    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        try {
            java.util.Date dt = d.getDate();
            String s1 = a.getText();
            String c = (String) category.getSelectedItem();
            if (dt != null && !s1.equals("") && !c.equals("")) {
                int amount = Integer.parseInt(s1);
                java.util.Date date = new java.sql.Date(dt.getTime());
                DB.DbConnection.s.executeUpdate("insert into spendings (s_category,S_date,amount) values('" + c + "',"
                        + "'" + date + "'," + amount + ")");
                JOptionPane.showMessageDialog(null, "Expense Added Successfully.");
                getEntries();
            } else {
                JOptionPane.showMessageDialog(null, "Plz Fill The Details! ");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_addActionPerformed

